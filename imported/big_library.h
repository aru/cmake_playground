static inline int aru_no_deps_sum(int a, int b) {
  return a + b;
}

static inline int aru_no_deps_sub(int a, int b) {
  return a - b;
}

static inline int aru_no_deps_mul(int a, int b) {
  return a * b;
}

static inline int aru_no_deps_div(int a, int b) {
  return a / b;
}

static inline int aru_no_deps_mod(int a, int b) {
  return a % b;
}